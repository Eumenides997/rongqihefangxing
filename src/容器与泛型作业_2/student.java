package 容器与泛型作业_2;

public class student {
	
	private String name;
	private int age;
	
	private void setname(String name) {
		this.name=name;
	}
	
	private String getname() {
		return name;
	}
	
	private void setage(int age) {
		this.age=age;
	}
	
	private int getage() {
		return age;
	}

}
