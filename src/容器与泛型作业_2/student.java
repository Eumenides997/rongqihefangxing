package 容器与泛型作业_2;

public class student {
	
	private String name;
	private int age;
	
	public student(String name,int age) {
		this.name=name;
		this.age=age;
	}
	
	public void setname(String name) {
		this.name=name;
	}
	
	public String getname() {
		return name;
	}
	
	public void setage(int age) {
		this.age=age;
	}
	
	public int getage() {
		return age;
	}

}
